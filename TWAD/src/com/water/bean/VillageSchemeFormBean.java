package com.water.bean;

public class VillageSchemeFormBean {
	
	private String villageSchemeId;
	private String villageName;
	private String schemeName;
	private String quantity;
	private int schemeId;
	private int villageId;
	
	public String getVillageSchemeId() {
		return villageSchemeId;
	}
	public void setVillageSchemeId(String villageSchemeId) {
		this.villageSchemeId = villageSchemeId;
	}
	public String getVillageName() {
		return villageName;
	}
	public void setVillageName(String villageName) {
		this.villageName = villageName;
	}
	public String getSchemeName() {
		return schemeName;
	}
	public void setSchemeName(String schemeName) {
		this.schemeName = schemeName;
	}
	public int getSchemeId() {
		return schemeId;
	}
	public void setSchemeId(int schemeId) {
		this.schemeId = schemeId;
	}
	public int getVillageId() {
		return villageId;
	}
	public void setVillageId(int villageId) {
		this.villageId = villageId;
	}
	public String getQuantity() {
		return quantity;
	}
	public void setQuantity(String quantity) {
		this.quantity = quantity;
	}
	
	
}
